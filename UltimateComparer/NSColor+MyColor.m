#import "NSColor+MyColor.h"

@implementation NSColor (MyColor)

+ (NSColor *)lightRedColor {
    return [NSColor colorWithCalibratedRed:0.973 green:0.624 blue:0.624 alpha:1.0];
}

+ (NSColor *)lightYellowColor {
    return [NSColor colorWithCalibratedRed:1.0 green:0.996 blue:0.482 alpha:1.0];
}

@end
